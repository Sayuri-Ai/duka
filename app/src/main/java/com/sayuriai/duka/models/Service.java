package com.sayuriai.duka.models;

public class Service {
    String name ;
    String id;

    public Service(String name, int id) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
